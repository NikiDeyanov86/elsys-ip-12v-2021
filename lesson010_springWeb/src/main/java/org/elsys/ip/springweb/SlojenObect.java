package org.elsys.ip.springweb;

public class SlojenObect {
    private final int id;
    private final String name;

    public SlojenObect(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
